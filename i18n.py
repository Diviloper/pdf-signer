def translate(language: str, key: str, **kwargs) -> str:
    """Look up ``key`` for ``language`` (falling back to the default
    language, then to the key itself) and format it with ``kwargs``."""
    table = TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE])
    text = table.get(key) or TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    return text.format(**kwargs) if kwargs else text
